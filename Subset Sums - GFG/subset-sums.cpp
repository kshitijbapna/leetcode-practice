class Solution
{
public:
    vector<int> subsetSums(vector<int> arr, int N)
    {
        long long int x=1LL<<N;
        vector<int> ans;
        for(int i=0;i<x;i++){
            int sum=0;
            for(int j=0;j<N;j++){
                if((i>>j)&1)
                sum+=arr[j];
            }
            ans.push_back(sum);
        }
        return ans;
    }
};

